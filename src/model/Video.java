package model;

/**
 *
 * @author LUNA
 */
public class Video {

    private String id;
    private String duracion;

    public Video(String id, String duracion) {
        this.id = id;
        this.duracion = duracion;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDuracion() {
        return duracion;
    }

    public void setDuracion(String duracion) {
        this.duracion = duracion;
    }

}
